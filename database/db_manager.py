@dataclass
class TrafficRecord:
    """
    Represents a single captured network packet / flow record.

    Fields map 1-to-1 with the ``traffic_records`` table columns.
    Optional fields default to None and are filled in by the capture layer.
    """
    timestamp: str                            # ISO-8601 capture timestamp
    src_ip: str                               # Source IP address
    dst_ip: str                               # Destination IP address
    src_port: int                             # Source port (0 if N/A)
    dst_port: int                             # Destination port
    protocol: str                             # Protocol name (TCP/UDP/ICMP…)
    protocol_num: int                         # Protocol number
    packet_length: int                        # Total packet size in bytes
    payload_length: int                       # Payload size in bytes
    ttl: Optional[int] = None                 # Time-To-Live value
    tcp_flags: Optional[str] = None          # TCP flags as hex string
    window_size: Optional[int] = None        # TCP window size
    icmp_type: Optional[int] = None          # ICMP message type
    dns_query: Optional[str] = None          # DNS query name (if applicable)
    http_method: Optional[str] = None        # HTTP method (if applicable)
    http_host: Optional[str] = None          # HTTP host header (if applicable)
    session_id: Optional[str] = None         # Capture session identifier
    label: str = "UNKNOWN"                   # Ground-truth or predicted label
    anomaly_score: Optional[float] = None    # ML anomaly score (Phase 2)
    id: Optional[int] = None                 # Auto-assigned DB primary key


@dataclass
class AlertRecord:
    """
    Represents a security alert generated by a detector or ML model.

    Alerts are stored in the ``alerts`` table and surfaced on the dashboard.
    """
    timestamp: str                            # ISO-8601 alert timestamp
    alert_type: str                           # e.g. "DDoS", "PortScan"
    severity: str                             # "LOW" | "MEDIUM" | "HIGH" | "CRITICAL"
    src_ip: str                               # Offending source IP
    dst_ip: str                               # Target IP
    dst_port: Optional[int] = None           # Target port (if applicable)
    protocol: Optional[str] = None           # Associated protocol
    description: str = ""                     # Human-readable alert description
    raw_evidence: Optional[str] = None       # JSON-serialised evidence blob
    is_acknowledged: bool = False            # Analyst acknowledgement flag
    acknowledged_at: Optional[str] = None   # ISO-8601 acknowledgement timestamp
    session_id: Optional[str] = None         # Associated capture session
    id: Optional[int] = None                 # Auto-assigned DB primary key


@dataclass
class SessionRecord:
    """
    Represents a single packet-capture session with aggregate statistics.
    """
    session_id: str                           # UUID for the session
    start_time: str                           # ISO-8601 session start
    end_time: Optional[str] = None           # ISO-8601 session end (None if live)
    interface: str = "unknown"               # Network interface used
    total_packets: int = 0                   # Total packets captured
    total_bytes: int = 0                     # Total bytes captured
    total_alerts: int = 0                    # Alerts raised in session
    pcap_file: Optional[str] = None         # Path to saved PCAP file
    notes: Optional[str] = None             # Analyst notes
    id: Optional[int] = None


@dataclass
class ModelMetadata:
    """
    Stores metadata for trained ML models (used in Phase 2).

    Enables the system to track model versions, performance metrics,
    and which model is currently active.
    """
    model_name: str                           # e.g. "IsolationForest_v1"
    model_type: str                           # e.g. "anomaly_detection"
    model_path: str                           # Absolute path to serialised model
    trained_at: str                           # ISO-8601 training timestamp
    accuracy: Optional[float] = None
    f1_score: Optional[float] = None
    precision: Optional[float] = None
    recall: Optional[float] = None
    training_samples: Optional[int] = None
    feature_names: Optional[str] = None      # JSON list of feature names
    is_active: bool = False                  # Currently deployed model flag
    notes: Optional[str] = None
    id: Optional[int] = None


