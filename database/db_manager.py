    def update_session_stats(
        self,
        session_id: str,
        *,
        total_packets: Optional[int] = None,
        total_bytes: Optional[int] = None,
        total_alerts: Optional[int] = None,
        end_time: Optional[str] = None,
    ) -> bool:
        """
        Update aggregate statistics for an existing session.

        Args:
            session_id:    The UUID of the session to update.
            total_packets: New packet count (replaces existing).
            total_bytes:   New byte count (replaces existing).
            total_alerts:  New alert count (replaces existing).
            end_time:      ISO-8601 end time (set when session closes).

        Returns:
            True if the record was updated; False if session not found.
        """
        updates: list[str] = []
        params: dict[str, Any] = {"session_id": session_id}

        if total_packets is not None:
            updates.append("total_packets = :total_packets")
            params["total_packets"] = total_packets
        if total_bytes is not None:
            updates.append("total_bytes = :total_bytes")
            params["total_bytes"] = total_bytes
        if total_alerts is not None:
            updates.append("total_alerts = :total_alerts")
            params["total_alerts"] = total_alerts
        if end_time is not None:
            updates.append("end_time = :end_time")
            params["end_time"] = end_time

        if not updates:
            return False

        sql = f"""
        UPDATE capture_sessions
        SET {', '.join(updates)}
        WHERE session_id = :session_id;
        """
        with self._transaction() as cursor:
            cursor.execute(sql, params)
            updated = cursor.rowcount > 0
        return updated

    # ── Maintenance ────────────────────────────────────────────────────────────

    def prune_old_records(self, keep_latest: Optional[int] = None) -> dict[str, int]:
        """
        Remove the oldest traffic records and alerts beyond the configured
        retention limits.

        Args:
            keep_latest: If provided, keep only this many of the most recent
                         traffic records. Otherwise uses the configured maximum.

        Returns:
            Dictionary with counts of deleted rows per table.
        """
        max_traffic = keep_latest or self._db_config.max_traffic_records
        max_alerts = self._db_config.max_alert_records

        deleted: dict[str, int] = {"traffic_records": 0, "alerts": 0}

        traffic_count = self.get_traffic_count()
        if traffic_count > max_traffic:
            excess = traffic_count - max_traffic
            with self._transaction() as cursor:
                cursor.execute(
                    """
                    DELETE FROM traffic_records
                    WHERE id IN (
                        SELECT id FROM traffic_records
                        ORDER BY timestamp ASC
                        LIMIT ?
                    );
                    """,
                    (excess,),
                )
                deleted["traffic_records"] = cursor.rowcount

        alert_count = self.get_alert_count()
        if alert_count > max_alerts:
            excess = alert_count - max_alerts
            with self._transaction() as cursor:
                cursor.execute(
                    """
                    DELETE FROM alerts
                    WHERE id IN (
                        SELECT id FROM alerts
                        ORDER BY timestamp ASC
                        LIMIT ?
                    );
                    """,
                    (excess,),
                )
                deleted["alerts"] = cursor.rowcount

        if any(deleted.values()):
            log.info("Pruned records: %s", deleted)

        return deleted

    # ── Model Metadata CRUD ───────────────────────────────────────────────────

    def insert_model_metadata(self, record: ModelMetadata) -> int:
        """
        Insert ML model metadata into the ``model_metadata`` table.

        Called by :class:`ModelManager` after each training run to persist
        model performance metrics and file paths to the database.

        Args:
            record: A :class:`ModelMetadata` dataclass instance.

        Returns:
            The primary key of the inserted row.

        Raises:
            sqlite3.Error: On insertion failure.
        """
        sql = """
        INSERT INTO model_metadata (
            model_name, model_type, model_path, trained_at,
            accuracy, f1_score, precision_score, recall_score,
            training_samples, feature_names, is_active, notes
        ) VALUES (
            :model_name, :model_type, :model_path, :trained_at,
            :accuracy, :f1_score, :precision, :recall,
            :training_samples, :feature_names, :is_active, :notes
        );
        """
        # ModelMetadata dataclass uses 'precision' and 'recall' field names.
        # SQL table uses 'precision_score' and 'recall_score' columns.
        # We bind them via :precision / :recall aliases in the SQL.
        data = asdict(record)
        data.pop("id", None)
        data["is_active"] = int(data["is_active"])
        # 'precision' and 'recall' keys already match the SQL binding aliases
        with self._transaction() as cursor:
            cursor.execute(sql, data)
            row_id: int = cursor.lastrowid  # type: ignore[assignment]
        log.debug(
            "Model metadata inserted: '%s' (id=%d).",
            record.model_name, row_id,
        )
        return row_id

    def fetch_model_metadata(
        self,
        model_type: Optional[str] = None,
        active_only: bool = False,
        limit: int = 50,
    ) -> list[dict[str, Any]]:
        """
        Retrieve ML model metadata records.

        Args:
            model_type:  Filter by model type (e.g. "anomaly", "classifier").
            active_only: If True, return only active models.
            limit:       Maximum number of rows to return.

        Returns:
            List of row dictionaries.
        """
        conditions: list[str] = []
        params: dict[str, Any] = {"limit": limit}

        if model_type:
            conditions.append("model_type = :model_type")
            params["model_type"] = model_type
        if active_only:
            conditions.append("is_active = 1")

        where_clause = ("WHERE " + " AND ".join(conditions)) if conditions else ""
        sql = f"""
        SELECT * FROM model_metadata
        {where_clause}
        ORDER BY trained_at DESC
        LIMIT :limit;
        """
        conn = self._get_connection()
        cursor = conn.execute(sql, params)
        rows = [dict(row) for row in cursor.fetchall()]
        log.debug("Fetched %d model metadata records.", len(rows))
        return rows


    def health_check(self) -> dict[str, Any]:
        """
        Run a lightweight health-check on the database connection.

        Returns:
            Dictionary with health status details.
        """
        try:
            conn = self._get_connection()
            conn.execute("SELECT 1;")
            traffic_count = self.get_traffic_count()
            alert_count = self.get_alert_count()
            return {
                "status": "healthy",
                "db_path": str(self.db_path),
                "db_exists": self.db_path.exists(),
                "db_size_bytes": self.db_path.stat().st_size if self.db_path.exists() else 0,
                "traffic_records": traffic_count,
                "total_alerts": alert_count,
                "unacknowledged_alerts": self.get_alert_count(unacknowledged_only=True),
            }
        except Exception as exc:
            log.error("Database health check failed: %s", exc)
            return {"status": "unhealthy", "error": str(exc)}

    def __repr__(self) -> str:
        return f"DatabaseManager(db_path='{self.db_path}')"
